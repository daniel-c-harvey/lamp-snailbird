<h1 class="pink header">Square Peg: A History</h1>
<h3 class="aqua date header">2024 02 19</h3>
<p>
    Square Peg is a project I started when I first moved from playing metal to writing and producing electronic music.  
    The three EPs are a progressive exploration of synthesis and sequencing possibilities as I learned how electronic 
    sounds could come alive.  A blend between sythwave and other dark styles, the music was made to be exactly what I 
    wanted to listen to while driving around the city at night.  This project has been an outlet for me to experiment 
    with writing more melodic music.
</p>
