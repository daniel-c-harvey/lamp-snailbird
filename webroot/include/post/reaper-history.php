<p>
    There were two pieces of gear that took my synth desk from a toy to a usable production environment. 
    The Novation SL MkIII, which I'll be talking a lot about, and the Roland TR-8S drum machine.  
    I'm picky with my drum sounds and while the hardware is great it was unreasonable to synthesize 
    all my drums live, I just couldn't spare the voices.  
    I had my favorite kick patch on the Minilogue, pinging the filter in resonance with a nice thumpy sweep, 
    but it felt cruel to give up a polysynth for a kick drum. 
    The TR allows me to sequence my own synthesized drum samples along side the ACB models, FM engines, etc.  
    I finally could get the drums I love intuitively going while I played.  
    I used the SL to write almost every song I committed to during the year, and began to appreciate the 
    instant ability to sequence and program new things while playing. 
    I'm no keyboard player, and tend to program all melodies and patterns save for fills and mushy ambient-type chords.
</p>
<p>
    The hardware sequencing opened up new doors, and started me thinking of new ways to use the SL.  
    The Novation Components software let's you set up any number of useful MIDI control configurations, 
    and REAPER lets you bind a great number of functions to any MIDI CC trigger, 
    so I began of thinking of ways to use the encoders and faders to control REAPER.  
    The obvious starting place was some FX controls, so I set up some filters and a sequenced amp gate that I could play from the keyboard while jamming.   
    It's great to be able to high-pass the whole rhythm section with one fader, instead of needing to twist several macro knobs as concurrently as possible.
</p>


