<h1 class="pink header">From Software, Hardware, to Everything at Once</h1>
<h3 class="aqua date header">2024 03 01</h3>
<p>    
    <a href="https://reaper.fm">REAPER</a> has become the beating heart of how 
    I make and play music, and to talk about some of my current use cases and 
    workflows for playing live with synthesizers, I thought it may make more 
    sense to start at the beginning.
</p>
<img class="centeredimage" src="images/REAPER/SIV-tracking-session.jpg" />
<p>
    I started using the software for basic multitrack recording of a metal band back in 
    2012 and then as I moved and lost access to a space to play drums, 
    I focused all my creativity into in-the-box synthwave-ish productions in REAPER. 
    Having never made or really even listened to very much electronic music, 
    my first influences were an eclectic blend of artists such as 
    <a href="https://autechre.bandcamp.com/track/further">Autechre</a>, 
    <a href="https://blankbanshee.bandcamp.com/track/b-start-up">Blank</a> 
    <a href="https://blankbanshee.bandcamp.com/track/java-clouds">Banshee</a>, 
    <a href="https://gost1980s.bandcamp.com/track/amys-wager">GoST</a>, 
    <a href="https://perturbator.bandcamp.com/track/corrupted-by-design">Perturbator</a>, 
    and <a href="https://dreamcatalogue.bandcamp.com/track/the-carving-2">Diamondstein</a>.
</p>
<img class="centeredimage" src="images/REAPER/the-end-as-you-perceive-it-to-be-reaper-session.png" />
<p>
    It took a long time working through the <a href="studio.php?page=square-peg-releases">Square Peg</a> 
    project to develop a deeper sense for electronic musical instruments, their uses, and their capabilities.  
    During this time, I spent a lot of time playing with simple free instruments, falling in love with 
    <a href="https://www.blaukraut.info/">BlauKraut's Charlatan</a>, 
    and learning how to sequence MIDI arrangements inside of REAPER.
</p>
